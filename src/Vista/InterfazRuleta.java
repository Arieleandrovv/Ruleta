    private void EmpezarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EmpezarActionPerformed
        int numero=ruedaGirando.aleatorio();
        int numIndividual;
        int numColor;
        int numParImpar;
        int numBajoAlto;
        int numDocena;
        int numColumna;
        int numTriple;
        int numDoble;
        int numEsquina;
        if(cane.isCasillero(numero)){            
            jLabelMostrarColor.setText(cane.getColor());
        }else if(caro.isCasillero(numero)){
            jLabelMostrarColor.setText(caro.getColor());
        }else if (cave.isCasillero(numero)){            
            jLabelMostrarColor.setText(cave.getColor());
        }
        
        
        jLabelMostrarNumero.setText(numero+"");
        //logica numero numIndividual
        numIndividual=apuesta.apuestaIndividual(arregloMesa[numero]);      
        //logica numColor        
        numColor=controladorJuego.apuestaColor(numero, arregloColor);
        //logica par impar
        numParImpar=controladorJuego.apuestaParImpar(numero, arregloParImpar);
        //logica alto bajo
        numBajoAlto=controladorJuego.apuestaBajoAlto(numero, arregloMitad);        
        // logica arregloDocena        
        numDocena=controladorJuego.apuestaDocena(numero, arregloDocena);
        // logica arregloColumna
        numColumna=controladorJuego.apuestaColumna(numero, arregloColumna);
        //logica apuesta triple
        numTriple=controladorJuego.apuestaTriple(numero, arregloTripleNumero);
        //logica apuesta doble numero
        numDoble=controladorJuego.apuestaDoble(numero, arregloDobleNumero);
        //logica esquina
        numEsquina=controladorJuego.apuestaEsquina(numero, arregloEsquina);
        //resultado
        cantidadFicha=cantidadFicha+numIndividual+numColor+numParImpar+numBajoAlto+numDocena+numColumna+numTriple+numDoble+numEsquina;
        
        jLabelGanancia.setText(numIndividual+numColor+numParImpar+numBajoAlto+numDocena+numColumna+numTriple+numDoble+numEsquina+"");
        jLabelMonto.setText(cantidadFicha+"");
        //reiniciando
        arregloMesa = new int[37];
        arregloColor= new int[2];
        arregloParImpar = new int[2];
        arregloMitad = new int[2];
        arregloDocena=new int[3];
        arregloColumna=new int[3];
        arregloTripleNumero = new int[2];
        arregloDobleNumero=new int[60];
        arregloEsquina=new int[22];
        for (int x=0;x< clickeados.size();x++){
            javax.swing.JLabel a=clickeados.get(x);
            a.setIcon(null);
        }
    }//GEN-LAST:event_EmpezarActionPerformed

